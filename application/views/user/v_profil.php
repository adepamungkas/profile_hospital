      <p align="justify">

        Undang-Undang Republik Indonesia Nomor 36 Tahun 2009 tentang Kesehatan
        mengatakan bahwa kesehatan adalah salah satu unsur kesejahteraan yang harus diwujudkan
        sesuai cita-cita Bangsa Indonesia sebagaimana dimaksud dalam Pancasila
        dan Undang-Undang Dasar Negara Republik Indonesia tahun 1945
        <br>
        <br>
        Undang-Undang Republik Indonesia Nomor 44 Tahun 2009 Tentang Rumah Sakit
        menyatakan bahwa pelayanan kesehatan merupakan hak setiap orang yang dijamin dalam
        Undang-Undang Dasar 1945 yang harus diwujudkan dengan upaya peningkatan derajat kesehatan
        masyarakat yang setinggi-tingginya.
        Pasal 7 menyatakan bahwa rumah sakit dapat didirikan oleh pemerintah, pemerintah daerah atau swasta.
        Rumah sakit yang didirikan oleh swasta harus berbentuk badan hukum yang kegiatan usahanya hanya
        bergerak di bidang perumah sakitan.
        <br>
        <br>
        RS.Mitra Husada adalah rumah sakit umum yang dimiliki oleh PT. Mitra Husada Bersama.
        PT. Mitra Husada Bersama berdiri tanggal 14 Nopember 2006, dengan Akta Notaris M Reza Berawi,
        SH Nomor 32, disyahkan dengan SK Menteri Hukum dan Hak Asasi Manusia Nomor W6-00001 HT 01.01-Tahun 2007.
        <br>
        Latar belakang berdirinya RS.Mitra Husada adalah adanya keprihatinan dari beberapa Dokter dan Paramedis
        di Pringsewu dan sekitarnya akan belum terdapatnya rumah sakit di Pringsewu yang cukup representatif,
        padahal animo masyarakat Pringsewu cukup tinggi, di samping dari data statistik dan studi kelayakan
        menunjukkan bahwa penduduk Pringsewu dan sekitarnya (Tanggamus, Pesawaran dan Lampung Tengah) masih membutuhkan
        rumah sakit.
        <br>
        <br>
        Rumah Sakit Mitra Husada terletak di Jalan Jend. Ahmad Yani No. 14 Pringsewu,
        berdiri di atas tanah seluas 17.701 m2, dengan luas bangunan 8.962,00 m2
        (luas bangunan lantai 1 : 5.771,50 m2 dan luas bangunan lantai 2 : 3.190,50 m2).
        Pembangunan fisik gedung lama RS.Mitra Husada dimulai 22 Maret 2007 dan selesai pada bulan Juni 2008.
        Pembangunan gedung baru sudah dimulai pada akhir bulan Desember 2013.
        Registrasi Rumah Sakit : 18 02 0 38 dikeluarkan oleh Departemen Kesehatan Republik Indonesia pada tanggal
        1 Oktober 2009. Izin Tetap Penyelenggaraan Rumah Sakit Nomor : 440/845/D.02/P/V/2015, dikeluarkan oleh
        Bupati Kabupaten Pringsewu pada tanggal 1 Juni 2015. Penetapan Kelas sebagai Rumah Sakit Umum dengan Klasifikasi
        Kelas C dengan Nomor Keputusan Menteri Kesehatan Republik Indonesia : 762/MENKES/SK/VI/2010,
        ditetapkan pada tanggal 24 Juni 2010, Sertifikat Akreditasi Rumah Sakit dengan Status Akreditasi lulus tingkat
        dasar Nomor : KARS-SERT/513/V/2013 dikeluarkan oleh Komisi Akreditasi Rumah Sakit pada tanggal 23 Mei 2013.
        <br>
        <br>
        Pada awal operasional pelayanan meliputi rawat jalan dan rawat inap dengan 57 tempat tidur, pada tanggal 15 Januari
        2009 mendapat izin dari Dinas Kesehatan Tanggamus untuk menyelenggarakan rawat inap dengan 100 tempat tidur.
        Pada tahun 2015 RS.Mitra Husada menyelenggarakan rawat inap dengan kapasitas tempat tidur menjadi 160 tempat tidur.
